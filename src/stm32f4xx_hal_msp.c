void DMATransferComplete(DMA_HandleTypeDef*hdma){
	if(hdma->Instance == DMA_CHANNEL_4) {
		//Disable UART DMA mode
		huart1.Instance->CR3 &= ~USART_CR3_DMAT;
		//Turn LD2 ON
		HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
	}
}

